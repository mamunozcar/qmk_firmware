// Process custom keycodes
bool process_record_user(uint16_t keycode, keyrecord_t *record) {
    switch (keycode) {
        case GUI_ALT:
            if (record->event.pressed) {
                register_code(KC_LGUI);
                register_code(KC_LALT);
            } else {
                unregister_code(KC_LALT);
                unregister_code(KC_LGUI);
            }
            return false;

        // Spanish characters using dead keys (works on macOS/Linux with US International layout)
        case SP_NTILDE:
            if (record->event.pressed) {
                tap_code16(LALT(KC_N));
                tap_code(KC_N);
            }
            return false;
        case SP_AACUTE:
            if (record->event.pressed) {
                tap_code16(LALT(KC_E));
                tap_code(KC_A);
            }
            return false;
        case SP_EACUTE:
            if (record->event.pressed) {
                tap_code16(LALT(KC_E));
                tap_code(KC_E);
            }
            return false;
        case SP_IACUTE:
            if (record->event.pressed) {
                tap_code16(LALT(KC_E));
                tap_code(KC_I);
            }
            return false;
        case SP_OACUTE:
            if (record->event.pressed) {
                tap_code16(LALT(KC_E));
                tap_code(KC_O);
            }
            return false;
        case SP_UACUTE:
            if (record->event.pressed) {
                tap_code16(LALT(KC_E));
                tap_code(KC_U);
            }
            return false;
    }
    return true;
}

